const FirstVar = 3;
const SecondVar = 5;
const operation1 = "+";
const operation2 = "-";
const operation3 = "*";
const operation4 = "/";

if (operation1 == "+") {
  console.log(`${FirstVar} + ${SecondVar} = ${FirstVar + SecondVar}`);
} else if ((operation2 = "-")) {
  console.log(`${FirstVar} - ${SecondVar} = ${FirstVar - SecondVar}`);
} else if (operation3 == "*") {
  console.log(`${FirstVar} * ${SecondVar} = ${FirstVar * SecondVar}`);
} else if (operation4 == "/") {
  console.log(`${FirstVar} / ${SecondVar} = ${FirstVar / SecondVar}`);
}
