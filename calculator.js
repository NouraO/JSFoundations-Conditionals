// Write if-statements here
const FirstVar = 3;
const SecondVar = 5;
const operation1 = "*";

if (operation1 === "+") {
  console.log(`${FirstVar} + ${SecondVar} = ${FirstVar + SecondVar}`);
} else if (operation1 === "-") {
  console.log(`${FirstVar} - ${SecondVar} = ${FirstVar - SecondVar}`);
} else if (operation1 === "*") {
  console.log(`${FirstVar} * ${SecondVar} = ${FirstVar * SecondVar}`);
} else if (operation1 === "/") {
  console.log(`${FirstVar} / ${SecondVar} = ${FirstVar / SecondVar}`);
}
